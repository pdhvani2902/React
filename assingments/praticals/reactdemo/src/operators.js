
    // Declare two numbers
let a = 15;
let b = 5;

// --- Arithmetic Operators ---
console.log("Arithmetic Operations:");
console.log("Addition: " + (a + b));
console.log("Subtraction: " + (a - b));
console.log("Multiplication: " + (a * b));
console.log("Division: " + (a / b));

// --- Comparison Operators ---
console.log("\nComparison Operations:");
console.log("Are a and b equal? " + (a == b));
console.log("Is a greater than b? " + (a > b));
console.log("Is a less than b? " + (a < b));

// --- Logical Operators ---
console.log("\nLogical Operations:");
console.log("Is a > 10 AND b < 10? " + (a > 10 && b < 10));
console.log("Is a > 10 OR b < 3? " + (a > 10 || b < 3));
console.log("NOT (a > b): " + !(a > b));

